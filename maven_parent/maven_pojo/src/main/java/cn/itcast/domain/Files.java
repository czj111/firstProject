package cn.itcast.domain;

public class Files {
    private String name;
    private String path;

    @Override
    public String toString() {
        return "Files{" +
                "name='" + name + '\'' +
                '}';
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
